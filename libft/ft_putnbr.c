#include "libft.h"

void	ft_putnbr(int n)
{
	char out;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			n = -n;
			write(1, "-", 1);
		}
		if (n >= 10)
			ft_putnbr(n / 10);
		out = n % 10 + '0';
		write(1, &out, 1);
	}
}
