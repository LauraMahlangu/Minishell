#include "libft.h"

int		ft_isempty(char *s)
{
	int		i;

	i = 0;
	if (ft_isspace(s[i]))
		while (ft_isspace(s[i]))
			i++;
	return ((i == (int)ft_strlen(s)) ? 1 : 0);
}
